// Decrement Operator (--)
// Post-Decrement (x--): Returns the current value of x and then decrements it.
// Pre-Decrement (--x): Decrements the value of x first, then returns the updated value.



// let x = 5;

// console.log(x--); // Output: 5 (returns current value, then decrements)
// console.log(x);   // Output: 4 (value after decrement)

// let y = 5;
// console.log(--y); // Output: 4 (decrements first, then returns the new value)
// console.log(y);   // Output: 4





// Operator	Description	           Example	                   Result
// x++	        Post-Increment    (returns value first)	        x = 5; x++	5
// ++x	        Pre-Increment     (increments first)	        x = 5; ++x	6
// x--	        Post-Decrement    (returns value first)	        x = 5; x--	5
// --x	        Pre-Decrement     (decrements first)	        x = 5; --x	4




